export class Friend {

   FirstName : string;
   LastName : string;
   email : string;
   PhoneNumber : string;
   Language : string;

  constructor(FirstName: string, LastName: string, email: string, PhoneNumber: string, Language: string) {
    this.FirstName = FirstName;
    this.LastName = LastName;
    this.email = email;
    this.PhoneNumber = PhoneNumber;
    this.Language = Language;
  }
}

